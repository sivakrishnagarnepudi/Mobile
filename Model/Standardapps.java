public class Standardapps {
    String google;
    String youtube;
    String settings;

    public Standardapps(String google,String youtube,String settings) {
        this.google = google;
        this.youtube=youtube;
        this.settings=settings;
    }

    public String getSettings() {
        return settings;
    }

    public String getGoogle() {
        return google;
    }

    public String getYoutube() {
        return youtube;
    }
}
